document.getElementById("btn1").addEventListener("click", function() {
    var linkList = document.getElementById("linkList");
    if (linkList.style.display === "block") {
        linkList.style.display = "none";
        linkList.style.paddingTop = "60px";
        linkList.style.animation = "slideDown 0.5s ease";
        document.getElementById("btn2").style.marginTop = "0";
    } else {
        linkList.style.display = "block";
        linkList.style.paddingTop = "50px";
        document.getElementById("btn2").style.marginTop = "180px";
    }
});
